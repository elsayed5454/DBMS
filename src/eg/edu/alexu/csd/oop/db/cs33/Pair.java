package eg.edu.alexu.csd.oop.db.cs33;

public class Pair{
	Object x;
	Object y;
	
	public Pair(Object x,Object y) {
		super();
		this.x=x;
		this.y=y;
	}
	
	public Object getX() {
		return x;
	}
	public void setX(Object x) {
		this.x = x;
	}
	public Object getY() {
		return y;
	}
	public void setY(Object y) {
		this.y = y;
	}
	

}
